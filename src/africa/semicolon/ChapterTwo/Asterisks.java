package africa.semicolon.ChapterTwo;

public class Asterisks {

    public static void main(String[] args) {
        System.out.println("*********    ***       *       *");
        System.out.println("*       *  *     *    ***     * *");
        System.out.println("*       *  *     *   *****   *   *");
        System.out.println("*       *  *     *     *    *     *");
        System.out.println("*       *  *     *     *   *       *");
        System.out.println("*       *  *     *     *    *     *");
        System.out.println("*       *  *     *     *     *   *");
        System.out.println("*       *  *     *     *      * *");
        System.out.println("*********   *****      *       *");

        System.out.println();

        //2.19
        System.out.printf("*%n**%n***%n****%n*****%n");

        System.out.println();

        //2.20
        System.out.println("*");
        System.out.println("***");
        System.out.println("*****");
        System.out.println("****");
        System.out.println("**");

        System.out.println();

        //2.21
        System.out.print("*");
        System.out.print("***");
        System.out.print("*****");
        System.out.print("****");
        System.out.println("**");

        System.out.println();

        //2.22
        System.out.print("*");
        System.out.println("***");
        System.out.println("*****");
        System.out.print("****");
        System.out.println("**");

        System.out.println();

        //2.23
        System.out.printf("%s%n%s%n%s%n", "*", "***", "*****");

        System.out.println();
    }
}
